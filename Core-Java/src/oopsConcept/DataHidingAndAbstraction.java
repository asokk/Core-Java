/**
 Data Hiding:
      Data hiding is a software development technique specifically used in object-oriented programming (OOP) to 
 hide internal object details (data members- variable holding the values). So that outside person can't access
 our data

 * It can be achieved by using private modifier.

 * Security can be achieved.

 Abstraction:
 		Hiding internal implementation details and just highlighting the set of services what we are offering
 	is called abstraction.

 * By using interface and abstract classes we achieve abstraction.
 
 * Main advantage of Abstraction is Security - No one knows our internal implementation.

 */


